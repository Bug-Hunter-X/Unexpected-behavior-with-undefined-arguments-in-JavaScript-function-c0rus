function foo(a, b) {
  if (a === null || b === null) {
    return null; // Handle null values
  }
  return a + b;
}

console.log(foo(null, 5)); // Output: null
console.log(foo(5, null)); // Output: null
console.log(foo(5, 5)); // Output: 10

//The bug is that the function doesn't handle undefined values, it only handles null values. This can lead to unexpected behavior if the function is called with undefined arguments.